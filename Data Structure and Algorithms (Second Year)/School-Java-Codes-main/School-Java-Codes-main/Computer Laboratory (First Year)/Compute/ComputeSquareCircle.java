public class ComputeSquareCircle {
  public static double computeCircumference(double r) {
    return 2 * Math.PI * r;
  }
  public static double computeArea(double r) {
    return Math.PI * r * r;
  }
  public static double computeDiameter(double r) {
    return 2 * r;
  }
  public static double computeSquareArea(double a) {
    return a * a;
  }
  public static double computeSquarePerimeter(double a) {
    return 4 * a;
  }
}
